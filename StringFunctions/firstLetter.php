<!-- ucfirst() and lcfirst()
Description: Capitalizes the first letter of a string (ucfirst()) or converts it to lowercase (lcfirst()). -->

<!-- First letter convert into Uppercase -->
<?php 
$string = "sazid";
$toUpper = ucfirst($string);
echo $toUpper;
?>

<!-- First letter convert into lowercase  -->
<?php 
$string = "Sazid";
$toLower = lcfirst($string);
echo $toLower;
?>
